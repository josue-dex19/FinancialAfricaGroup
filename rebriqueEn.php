                <p>
                    <strong>Background:</strong>
                    <br>
                    <p>The organization of the Trade Fair of the Member States of the Organization of Islamic Cooperation 
(OIC) falls within the framework of the decisions and recommendations issued in the field of economic 
cooperation among the OIC Member States and approved by the Islamic Summit Conferences and the 
various sessions of the Standing Committee for Economic and Commercial Cooperation among the OIC 
Member States (COMCEC) as well as the Conferences and Councils of Foreign Ministers</p>

                <p>
                The Trade Fair of the OIC Member States, which has been held every two years since 1984 in 
Casablanca, is the biggest trade event of the Organization of the Islamic Cooperation. It allows 
exhibitors from the 57 member countries to exchange the ways and means to boost trade and 
investments.


                </p>
                <p>
                This fair is organized in collaboration with the Ministry in charge of foreign trade alongside the local 
fair organizing body and the OIC Institutions and other partners in trade development.


                </p>
                <p>
                In this context, the COMCEC adopted a recommendation in 2002 requesting the Islamic Centre for 
Development of Trade to organize a trade fair for the Member States of the Organization of Islamic 
Cooperation every two years in one of the Member States.


                </p>
                <p>This multi-sectorial Fair constitutes a platform for the business community, Trade Promotion Organs, 
Investment Promotion Agencies, Chambers of Commerce, Industry, Agriculture and Handicrafts, 
professional associations, and economic operators of the Member States for the promotion of their 
products and services, conclusion of commercial transactions and establishment of partnership relations.

                </p>
                <p>
                    <strong>Objectives:</strong> The present Fair offers an opportunity to the OIC Member States: 
                    <ul>
                        <li>
                        To exhibit products and services in various sectors with a view to developing and sustaining
trade relations between the business communities of OIC countries. 
                        </li>
                        <li>
                        To strengthen regional economic integration, by targeting key sectors allowing the development 
of value chains and sectoral complementarities within the OIC space. 
                        </li>
                        <li>
                        To have access to information on market potentialities.
                                            </li>
                        <li> To make direct contact between economic players from the public and private sectors;
</li>
                        <li>  To exchange experiences between business communities, in order to create conditions for 
innovation in the field of promotion and marketing of products and services.
</li>
                    </ul>
                </p>
                <p>
                The previous editions of the Fair, which represented a space for exchanges and meetings, were a great 
success. The last three editions of the Fair were held in Iraq, Kuwait, and Saudi Arabia, and have 
witnessed wide participation of the public and private sectors of about 40 countries with more than 300 
companies.
</p>
                <p>This 17th edition has particular importance, as it represents the 1st Edition after the COVID period. It 
will be placed under the high patronage of His Excellency Mr. Macky Sall President of the Republic of 
Senegal and will know the participation of no less than 10,000 professional visitors on an area of 20,000 
m2 including 8,000 m2 dedicated to the exhibition<p>
                    <strong>Participants</strong>
                    <ul>
                        <li> The Fair is open to the participation of the 57 Member States of the Organization of Islamic 
Cooperation (OIC) and to the economic operators of these countries.</li>
                        <li> The countries with observer status in the OIC (Bosnia-Herzegovina, Central African Republic, 
Kingdom of Thailand, Russian Federation, Turkish Northern Cyprus, as well as Muslim
economic operators from non-Member States, may also participate in the Fair. By mutual 
agreement, the organizers may extend invitations to participants from non-OIC member 
countries as guests.</li>
                    </ul>
                </p>
                <p>
                    <strong>Products exhibited</strong>
                    <p>The products and services covered by the Fair or Exhibition, originating from the OIC Member States, 
as defined by the national regulations in force in the host country and in conformity with the ethics and 
values of Islam, shall be admitted to the Fair.
                    </p>
                </p>
                <p>
                    <strong>Promotional Campaign of the Fair  </strong>
                    <ul>
                    The organizers are conducting a promotional campaign through:
                         <li>
                         A vast information and awareness campaign through the written and audiovisual press in the 
host country as well as at the level of the Member States (press conference, TV and radio and 
electronic spots, Internet site, articles), 
                        </li>
                        <li>Awareness raising and promotional activities during seminars, forums, and OIC institutional 
meetings.
</li>
                        <li>  Promotional actions during the specialized fairs and exhibitions organized unilaterally and, in 
the fairs, and exhibitions in which they will take part.
</li>
                        <li>  Both parties agree to work together to prepare the tools and materials for the promotion of the 
Fair: brochures/leaflets, participation forms, and links on both websites. </li>
The ICDT will distribute a questionnaire to all participants during the event to evaluate its performance 
and to elaborate on ways of improvement for future editions.
                    </ul>
                </p>
                <p>
                    <strong>Types of Activities Organized on the Sidelines of the Fair:

                    </strong>
                    <ul>
                        <li>  Meeting of the Trade Promotion Organs of the OIC Member States</li>
                        <li> Meeting of Investment Promotion Agencies (IPAs)
                        </li>
                        <li> Meeting of Private Sector organized by the Islamic Chamber of Commerce, Industry and 
Agriculture (ICCIA)
</li>
                        <li>  Investment Forums
                        </li>
                        <li> Training workshops on priority sectors

                        </li>
                        <li> Distribution of trophies to the most deserving pavilions according to the following criteria: 
exhibition area, quality of products and services to be exposed, number of exhibiting companies, 
the importance of the commercial delegation, regularity in the participation in the Fair.
                        </li>
                        <li>Workshops and seminars </li>
                        <li> B2B meetings and deal rooms
                        </li>
                        <li>  Awareness on the services offered by ICDT and other OIC institutions.
                        </li>
                    </ul>
