                <h3>1-Contexte</h3>
                <p>
                    Dans un contexte continental caractérisé par des perspectives de redressement post-Covid de la croissance - 3,8% en 2022 pour l’Afrique subsaharienne selon les dernières actualisations du FMI – le continent retrouve une véritable dynamique économique
                    dans la tendance de la reprise globale. C’est dans cette conjoncture, bien que fragilisée par les incertitudes relatives à la pandémie, que la plupart des secteurs productifs continuent de performer. Les orientations fortes relatives
                    à l’industrialisation et les politiques de renforcement des chaînes de valeur régionales ainsi que l’implémentation de la Zone de Libre Echange Continentale Africaine (ZLECAf), devraient constituer des facteurs avantageux pour la compétitivité
                    économique globale du continent.
                </p>
                <p>
                    C’est dans ce contexte continental que s’inscrit le Forum OCI-Sénégal, prévu à Dakar les 16 & 17 Juin 2022, en marge de la 17ème foire commerciale des Etats membres de l’OCI. Ce rendez-vous de haut niveau entend marquer la relance économique dans les
                    57 pays membres de l’Organisation de la Coopération Islamique (OCI).
                </p>
                <p>
                    Cet événement majeur est l’occasion de renforcer les relations commerciales entre le Sénégal et les autres pays membres de l’OCI, et réunira des acteurs économiques stratégiques : acteurs publics et privés de l’investissement, organes de promotion du
                    commerce et de l’investissement, associations professionnelles…
                </p>
                <p>
                    Dans cet esprit, le Sénégal fait partie des économies les plus attractives en raison de son positionnement géographique stratégique, mais aussi de sa stabilité politique et son climat des affaires des plus favorables. Les réformes structurelles ont permis
                    une modification importante du paysage économique du Sénégal, notamment grâce aux privatisations de nombreuses entreprises publiques dans la filière agricole et dans les infrastructures. En termes de performance, l’économie sénégalaise
                    a montré une véritable résilience pendant la crise sanitaire avec une croissance de plus de 6% en 2021, et se projette sur un taux de 10,8% en 2023 selon le Fonds Monétaire International (FMI). En parallèle, les flux entrants d’Investissements
                    Directs Etrangers (IDE) ont progressé de 39% en 2020, pour une valeur totale estimée à 1,5 milliard $US en 2020 d’après les dernières actualisations de la Conférence des Nations Unies sur le Commerce et le Développement (CNUCED).
                </p>
                <p>
                    En effet, le secteur primaire emploie 46% de la population active et contribue à 15,6% du PIB. Les principales cultures du Sénégal sont les arachides, le niébé, le manioc, les pastèques, le mil, le riz et le maïs. Le secteur secondaire contribue à 24,1%
                    du PIB et emploie 18% de la population active. Il est fondé majoritairement sur la production d’engrais et d’acide phosphorique à destination de l’Inde et sur la transformation de l’arachide (huile et tourteaux pour le bétail) et des
                    produits de la mer (malgré une raréfaction croissante de la ressource). Le secteur tertiaire contribue à 60,3% du PIB et emploie 22,4% de la population active. Il bénéficie en particulier de l’excellence des infrastructures de télécommunication,
                    qui favorise des investissements dans les téléservices et Internet.
                </p>
                <p>
                    Les projets phares du Sénégal dans la phase II du PSE sont :
                    <ul>
                        <li>Développement de l'habitat social et d'un écosystème de la construction ;</li>
                        <li> Hub Régional Multiservices et Tourisme ;</li>
                        <li>Mines et Fertilisants (Programme national mines artisanales, Relance du projet intégré sur le fer (Falémé), Développement de la filière phosphate et fertilisants, Accélération de l’exploitation du secteur aurifère, Accélération
                            de l'exploitation des gisements de zircon ;</li>
                        <li>Hub Logistique et Industriel Régional (3 Plateformes industrielles intégrées) ;</li>
                        <li>Agriculture, produits de la mer et agro-alimentaire (3 pôles industriels intégrés de transformation de produits de la mer, Développement accéléré de l'Aquaculture, Développement de trois corridors céréaliers- Restructuration de
                            la filière arachide, Création de 3 agropoles intégrées) ;</li>
                        <li>Redressement stratégique des secteurs de l’énergie et des infrastructures (Approvisionnement en hydrocarbures, Plan de relance intégré de l'électricité et Service universel de l’énergie).</li>
                    </ul>
                </p>
