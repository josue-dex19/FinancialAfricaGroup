                <h3>INTRODUCTION:</h3>
                <strong>SMIIC</strong>
                <br>
                <br>
                <p>The Standards and Metrology Institute for Islamic Countries (SMIIC) is an intergovernmental standardization organization affiliated with the Organisation of Islamic Cooperation (OIC). SMIIC, as a sound mechanism for harmonization of standards
                    among the OIC Countries and for the preparation of new ones, aims at realizing common standards, achieving uniformity in metrology, laboratory testing, and establishing accreditation schemes for the purpose of expediting the exchange
                    of materials, manufactured goods and products in the Muslim World under the umbrella of the OIC.</p>
                <p>Standards are key tools for people to reach products with minimum cost but with quality. Harmonization of standards, metrology, and accreditation is vital for accelerating the development of Muslim country economies. Its use will promote
                    common understanding throughout all member states. Common standards, metrology, and accreditation framework eliminates trade barriers and facilitates trade between the OIC member states and the entire world. In an age of globalization
                    and economic interdependence, Islamic countries need to work together to establish fully functional and sustainable knowledge-based economies. This is why The Standards and Metrology Institute for Islamic Countries (SMIIC) is mandated
                    by OIC to act as a professional entity in studying standardization, metrology, and accreditation fields and developing standards that will display the interests of the Islamic World.</p>
                <br>
