                <strong>ICDT</strong>
                <br>
                <br>
                <p>The Islamic Centre for Development of Trade (ICDT), established in Casablanca in 1984, is the subsidiary Organ of the OIC responsible for promoting trade and investments in the OIC countries. With more than 38 years of experience serving
                    public and private operators of OIC Member States, ICDT works to encourage regular trade exchanges within the OIC Region, promote investments likely to develop trade flows, contribute to building public and private sector capacity
                    in trade and investment-related areas and assisting the Member States in trade promotion and negotiations.</p>
                <p>The development of the Halal sector is at the heart of ICDT's activities through the organization of a capacity building program on raising awareness of Halal certification, the organization of a Halal Exhibition and B2B forums and meetings
                    every year in an OIC country as well as the publication of an annual report on the industry of the sector for the benefit of the public and private sectors of the Member States.</p>
                <p>The ICDT organizes during 2022, the 9th Edition of the OIC Halal Exhibition in Tunis from September 14 to 18, 2022, Halal Bahrain from October 6 to 8, 2022, and the 10th edition of the OIC Halal Exhibition in Istanbul from November 24
                    to 27, 2022.</p>
