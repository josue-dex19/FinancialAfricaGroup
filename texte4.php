                <strong>Halal</strong>
                <br>
                <br>
                <p>Over the last decade, the halal industry has gone global and we all watched and experienced how it occurred. And by witnessing its expansion, we became more accountable for our roles and tasks as the actors in the halal industry. With
                    this regard, SMIIC, as the unity of 46 countries, approaches to halal in line with its new vision which is “Creating a quality infrastructure that improves the economy, welfare and protects our Member States”.</p>
                <p>This vision also points out that halal is part of quality infrastructure. The standards are not fiqh documents but include Islamic Rules and within SMIIC, halal is perceived as a technical issue thereof, halal standards are developed within
                    SMIIC Technical Committees where various standard projects on halal are studied by both technical and Islamic experts from 46 Member States and liaison organization such as International Islamic Fiqh Academy.</p>
                <strong>Capacity Building Training</strong>
                <br>
                <br>
                <p>In line with SMIIC Strategy Plan 2021-2030, SMIIC coordinates and organizes projects, meetings, and training for Technical Capacity Building for its members. Within this strategic objective, SMIIC itself or in cooperation with other OIC
                    Institutions has organized capacity-building training since its establishment.</p>
                <strong>ORGANIZERS:</strong>
                <br>
                <br>
                <p>In the frame of the 17 Trade Fair of OIC Member States which will be held on 13-19 June 2022, in Dakar, SENEGAL, OIC/SMIIC Halal Foundation Training for Africa Region on "OIC/SMIIC 1:2019 General Requirements for Halal Food" will be organized
                    by SMIIC and ICDT on 15-16 June 2022 at Centre International du Commerce Extérieur du Sénégal (CICES).</p>
                <strong>OBJECTIVES:</strong>
                <br>
                <br>
                <p>The objective of this training program is to encourage the harmonization of Halal Standards used in the OIC and internationally by aiding in the adoption and use of the OIC/SMIIC Halal Standards. The program aims to inform the trainees
                    regarding the procedures for adoption, implementation, and use of the "OIC/SMIIC 1:2019 - General Requirements for Halal Food" whilst providing foundation training in Technical Committee (TC) work and auditing.</p>
                <strong>EXPECTED OUTPUTS:</strong>
                <br>
                <br>
                <p>The expected outputs are:</p>
                <ul>
                    <li>The trainees will be trained in:
                        <ul>
                            <li>The adoption, implementation, and use of OIC/SMIIC 1:2019</li>
                            <li>Introduction to SMIIC Information System, TC Work, and preparation of standards</li>
                            <li>Introduction to OIC/SMIIC 2:2019 – Conformity Assessment – Requirements for Bodies Providing Halal Certification</li>

                        </ul>

                    </li>
                    <li>The number of OIC Member States who have adopted and/or are using the OIC/SMIIC Standards on Halal has increased which will in turn facilitate intra-OIC trade.</li>

                </ul>
